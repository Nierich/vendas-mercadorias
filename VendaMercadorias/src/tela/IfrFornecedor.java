    private void btnSalvarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSalvarActionPerformed
        //declarando as variaveis
        String nomeFornecedor = tfdNomeFornecedor.getText();
        String emailFornecedor = tffEmailFornecedor.getText();
        String telefoneFornecedor = tffTelefoneFornecedor.getText();
        String cnpjFornecedor = tffCnpjFornecedor.getText();

        //Criação do objeto FORNCEDOR
        Fornecedor fornecedor = new Fornecedor();
        fornecedor.setId(idFornecedor);
        fornecedor.setNome(nomeFornecedor);
        fornecedor.setEmail(emailFornecedor);
        fornecedor.setTelefone(telefoneFornecedor);
        fornecedor.setCnpj(cnpjFornecedor);
        
        //Criação do objeto cidadeDAO
        FornecedorDAO fornecedorDAO = new FornecedorDAO();

        if (idFornecedor == 0) {

            if (fornecedorDAO.salvar(fornecedor) == null) {  //Representa uma inserção
                tfdNomeFornecedor.setText("");
                tffEmailFornecedor.setText("");
                tffTelefoneFornecedor.setText("");
                tffCnpjFornecedor.setText("");

                JOptionPane.showMessageDialog(this, "Fornecedor registrado com sucesso!");

                tfdNomeFornecedor.requestFocus();
            } else {
                JOptionPane.showMessageDialog(this, "Problemas no registro de fornecedor");
            }
        } else {
            if (fornecedorDAO.atualizar(fornecedor) == null) {  //Representa uma inserção
                tfdNomeFornecedor.setText("");
                tffEmailFornecedor.setText("");
                tffTelefoneFornecedor.setText("");
                tffCnpjFornecedor.setText("");

                JOptionPane.showMessageDialog(this, "Fornecedor atualizado com sucesso!");

                tfdNomeFornecedor.requestFocus();
            }
        }

        idFornecedor = 0;
    }//GEN-LAST:event_btnSalvarActionPerformed

