    private void btnExcluirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnExcluirActionPerformed
        String idTabela = String.valueOf(tblFornecedor.getValueAt(tblFornecedor.getSelectedRow(), 0));

        idFornecedor = Integer.parseInt(idTabela);
        
        if(new FornecedorDAO().excluir(idFornecedor) == null){
            JOptionPane.showMessageDialog(this, "Fornecedor excluido com sucesso!");
            
            new FornecedorDAO().popularTabela(tblFornecedor, "");
        } else{
            JOptionPane.showMessageDialog(this, "Problemas aos excluir");
        }
        idFornecedor = 0;
    }//GEN-LAST:event_btnExcluirActionPerformed

    private void btnEditarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEditarActionPerformed
        String idTabela = String.valueOf(tblFornecedor.getValueAt(tblFornecedor.getSelectedRow(), 0));

        idFornecedor = Integer.parseInt(idTabela);

        Fornecedor fornecedor = new FornecedorDAO().consultarId(idFornecedor);

        if (fornecedor != null) {
            jTabbedPane1.setSelectedIndex(1);
            
            
            tfdNomeFornecedor.setText(fornecedor.getNome());
            tffEmailFornecedor.setText(fornecedor.getEmail());
            tffTelefoneFornecedor.setText(fornecedor.getTelefone());
            tffCnpjFornecedor.setText(fornecedor.getCnpj());
                
            tfdNomeFornecedor.requestFocus();
        } else {
            JOptionPane.showMessageDialog(this, "Id da cidade não encontrado!");
        }
    }//GEN-LAST:event_btnEditarActionPerformed

