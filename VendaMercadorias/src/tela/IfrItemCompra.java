    private void tffQuantidadeItemFocusLost(java.awt.event.FocusEvent evt) {//GEN-FIRST:event_tffQuantidadeItemFocusLost
          
        String text = tffQuantidadeItem.getText();
        try {
            double quantidade = Double.parseDouble(text);
            DecimalFormat decimalFormat = new DecimalFormat("#,##0.00");
            String formattedText = decimalFormat.format(quantidade);
            tffQuantidadeItem.setText(formattedText);
        } catch (NumberFormatException ex) {
            // Lidar com o caso em que o texto não é um número válido
            tffQuantidadeItem.setText("");
        }
    }//GEN-LAST:event_tffQuantidadeItemFocusLost

private boolean ValidarData(){
        if (!Validacao.validarDataFormatada(tffDataItemCompra.getText())) {
            tffDataItemCompra.setBackground(Color.YELLOW);
            return false;
        } else {
            tffDataItemCompra.setBackground(Color.WHITE);
            return true;
        }
    }
    
    @Override
public void definirValor(String[] valores, String itemPesquisa) {
        if (itemPesquisa.equalsIgnoreCase("fornecedor")) {
            tfdIdFornecedor.setText(valores[0]);
            tfdNomeFornecedor.setText(valores[1]);
        } else if (itemPesquisa.equalsIgnoreCase("produto")) {
            tfdIdProduto.setText(valores[0]);
            tfdNomeProduto.setText(valores[1]);
            tfdValorProduto.setText(valores[2]);
        }
    }


