    private void btnInserirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnInserirActionPerformed

        ItemCompra itemCompra = new ItemCompra();

        itemCompra.setCompra_id(idItemCompra);
        itemCompra.setProduto_id(Integer.parseInt(tfdIdProduto.getText()));
        itemCompra.setQtde(Double.parseDouble(tffQuantidadeItem.getText().replace(',', '.')));
        itemCompra.setValor(Double.parseDouble(tfdValorProduto.getText().replace("R$", "").replace(',', '.')));

        if (new ItemCompraDAO().salvar(itemCompra) == null) {
            JOptionPane.showMessageDialog(null, "Item inserido.");

            new ItemCompraDAO().popularTabela(tblItem, "", idItemCompra);

            total += (itemCompra.getValor() * itemCompra.getQtde());

            tffTotal.setText(String.valueOf(total));

            tfdIdProduto.setText("");
            tfdNomeProduto.setText("");
            tfdValorProduto.setText("");
            tffQuantidadeItem.setText("");
        } else {
            JOptionPane.showMessageDialog(null, "Erro na inserção.");
        }
    }//GEN-LAST:event_btnInserirActionPerformed

    private void btnRemoverActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnRemoverActionPerformed
        if (tblItem.getSelectedRow() < 0) {
            JOptionPane.showMessageDialog(null, "Selecione um registro.");
        } else {
            int idItemC = Integer.parseInt(String.valueOf(tblItem.getValueAt(tblItem.getSelectedRow(), 0)));

            System.out.println("OSIt: " + idItemC);

            String retorno = new ItemCompraDAO().excluir(idItemC);

            if (retorno == null) {
                JOptionPane.showMessageDialog(null, "Registro removido.");

                String qtdeString = String.valueOf(tblItem.getValueAt(tblItem.getSelectedRow(), 3));
                String valorString = String.valueOf(tblItem.getValueAt(tblItem.getSelectedRow(), 4));

                // Remover caracteres não numéricos
                double qtde = Double.parseDouble(qtdeString.replaceAll("[^\\d.]", ""));
                double valor = Double.parseDouble(valorString.replaceAll("[^\\d.]", ""));

                total -= (qtde * valor);

                tffTotal.setText(String.valueOf(total));

                new ItemCompraDAO().popularTabela(tblItem, "", idItemCompra);
            } else {
                JOptionPane.showMessageDialog(null, "Erro: Problemas ao remover registro.\n\n" + retorno);
            }
        }
    }//GEN-LAST:event_btnRemoverActionPerformed

