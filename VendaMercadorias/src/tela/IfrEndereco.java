    private void btnEditarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEditarActionPerformed
        String idTabela = String.valueOf(tblEndereco.getValueAt(tblEndereco.getSelectedRow(), 0));

        idEndereco = Integer.parseInt(idTabela);

        Endereco endereco = new EnderecoDAO().consultarId(idEndereco);

        if (endereco != null) {
            jTabbedPane1.setSelectedIndex(1);

            tfdDescricaoEndereco.setText(endereco.getDescricao());
            tffDescricaoCep.setText(endereco.getCep());

            tfdDescricaoEndereco.requestFocus();
        } else {
            JOptionPane.showMessageDialog(this, "Id da cidade não encontrado!");
        }
    }//GEN-LAST:event_btnEditarActionPerformed

    private void btnExcluirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnExcluirActionPerformed
        String idTabela = String.valueOf(tblEndereco.getValueAt(tblEndereco.getSelectedRow(), 0));

        idEndereco = Integer.parseInt(idTabela);
        
        if(new EnderecoDAO().excluir(idEndereco) == null){
            JOptionPane.showMessageDialog(this, "Endereço excluido com sucesso!");
            
            new EnderecoDAO().popularTabela(tblEndereco, "");
        } else{
            JOptionPane.showMessageDialog(this, "Problemas aos excluir");
        }
        idEndereco = 0;
    }//GEN-LAST:event_btnExcluirActionPerformed

    private void tffDescricaoCepFocusLost(java.awt.event.FocusEvent evt) {//GEN-FIRST:event_tffDescricaoCepFocusLost
        if (!Validacao.validarCEP(Formatacao.removerFormatacao(tffDescricaoCep.getText()))) {
            tffDescricaoCep.setBackground(Color.YELLOW);
        } else {
            tffDescricaoCep.setBackground(Color.WHITE);
        }
    }//GEN-LAST:event_tffDescricaoCepFocusLost


