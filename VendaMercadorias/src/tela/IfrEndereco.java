    private void btnSalvarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSalvarActionPerformed
        //declarando as variaveis
        String nomeEndereco = tfdDescricaoEndereco.getText();
        String cepEndereco = tffDescricaoCep.getText();

        //Criação do objeto cidade
        Endereco endereco = new Endereco();
        endereco.setId(idEndereco);
        endereco.setCep(cepEndereco);
        endereco.setDescricao(nomeEndereco);

        //Criação do objeto cidadeDAO
        EnderecoDAO enderecoDAO = new EnderecoDAO();

        if (idEndereco == 0) {

            if (enderecoDAO.salvar(endereco) == null) {  //Representa uma inserção
                tfdDescricaoEndereco.setText("");
                tffDescricaoCep.setText("");

                JOptionPane.showMessageDialog(this, "Endereço registrado com sucesso!");

                tfdDescricaoEndereco.requestFocus();
            } else {
                JOptionPane.showMessageDialog(this, "Problemas no registro de endereço");
            }
        } else {
            if (enderecoDAO.atualizar(endereco) == null) {  //Representa uma inserção
                tfdDescricaoEndereco.setText("");
                tffDescricaoCep.setText("");

                JOptionPane.showMessageDialog(this, "Endereço atualizado com sucesso!");

                tfdDescricaoEndereco.requestFocus();
            }
        }

        idEndereco = 0;
    }//GEN-LAST:event_btnSalvarActionPerformed

