    private void BtnAcessarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BtnAcessarActionPerformed
        
        if (tffEmailUsuario.getText().trim().length() > 0
                && String.valueOf(tfpSenhaUsuario.getPassword()).trim().length() > 0) {

            Usuario u = new Usuario();
            u.setEmail(tffEmailUsuario.getText());
            u.setSenha(String.valueOf(tfpSenhaUsuario.getPassword()));

            if (new UsuarioDAO().autenticar(u)) {
                new FrmPrincipal().setVisible(true);

                this.dispose();
            } else {
                JOptionPane.showMessageDialog(this, "Usuário ou senha incorretos!");
            }
        } else {
            JOptionPane.showMessageDialog(this, "Preencha usuário e senha adequadamente.");

            tffEmailUsuario.requestFocus();
        }
    }//GEN-LAST:event_BtnAcessarActionPerformed

