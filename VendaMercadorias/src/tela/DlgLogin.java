    private void tffEmailUsuarioFocusLost(java.awt.event.FocusEvent evt) {//GEN-FIRST:event_tffEmailUsuarioFocusLost
        if (!Validacao.validarEmail(tffEmailUsuario)) {
            tffEmailUsuario.setBackground(Color.YELLOW);
        } else {
            tffEmailUsuario.setBackground(Color.WHITE);
        }
    }//GEN-LAST:event_tffEmailUsuarioFocusLost

